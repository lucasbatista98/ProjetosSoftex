// Definindo os tipos de variáveis
const nome = "João"; // string
const salario = 2500.5; // number
const idade = 30; // number
const possuiDiploma = true; // boolean

// Exemplo de cadastro de pessoa
const pessoa = {
  nome: nome,
  salario: salario,
  idade: idade,
  possuiDiploma: possuiDiploma,
};

// Exibindo os dados da pessoa cadastrada
console.log("Nome: " + pessoa.nome); // Saída: Nome: João
console.log("Salário: " + pessoa.salario); // Saída: Salário: 2500.5
console.log("Idade: " + pessoa.idade); // Saída: Idade: 30
console.log("Possui Diploma: " + pessoa.possuiDiploma); // Saída: Possui Diploma: true
